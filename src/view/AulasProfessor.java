    public void preecherCombo() {
        try {
            professores = daoObject.findAll();
            for (Professor obj : professores) {
                combo.addItem(obj.getFuncionario().getPessoa().getNome()+" "+obj.getFuncionario().getPessoa().getSobrenome());
            }
        } catch (BussinessException ex) {
            pai.control.messageErroBussiness(ex);
        }
    }

