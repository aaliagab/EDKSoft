    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            List<AulasProfessorReport> objs = new ArrayList<>();
            Professor ex = professores.get(combo.getSelectedIndex());
            List<Aula> dist = pai.control.getAulaDAO().findAll();
            for (Aula obj : dist) {
                objs.add(new AulasProfessorReport(obj));
            }
            try {
                JasperReport reporte = null;
                URL path = this.getClass().getResource("/reports/aulas_report.jasper");
                Map<String, Object> parametros = new HashMap<>();
                parametros.put("logo", this.getClass().getResource("/resource/logo.jpg").toString());
                parametros.put("professor", ex.getFuncionario().getPessoa().getNome()+" "+ex.getFuncionario().getPessoa().getSobrenome());
                reporte = (JasperReport) JRLoader.loadObject(path);
                JasperPrint jprint = JasperFillManager.fillReport(reporte, parametros, new JRBeanCollectionDataSource(objs));
                JasperViewer jviewer = new JasperViewer(jprint, false);
                jviewer.setVisible(true);
                jviewer.setTitle("aulas_report");

            } catch (Exception e) {
                msg = new Toast(e.getMessage(), 2000);
                msg.showToast();
            }
        } catch (BussinessException ex) {
            pai.control.messageErroBussiness(ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

