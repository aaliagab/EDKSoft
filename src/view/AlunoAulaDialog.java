    private void atribuirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atribuirActionPerformed
        int[] rows = tabela.getSelectedRows();

        if (rows.length == 0) {
            msg = new Toast("Você tem que fazer uma seleção dos alunos a asignar para a aula", 3000);
            msg.showToast();
        } else {
            try {
                for (int row : rows) {
                    AlunoAula obj = new AlunoAula(list_atual.get(row),
                            aula);

                    main.control.getAlunoAulaDAO().save(obj);
                    aula = main.control.getAulaDAO().get(aula.getId());
                }

                updateList(aula);
                updateJTable();
                updateJTableAtribuida();

            } catch (BussinessException ex) {
                main.control.messageErroBussiness(ex);
            }

        }
    }//GEN-LAST:event_atribuirActionPerformed

    private void naoAtribuirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_naoAtribuirActionPerformed
        int[] rows = tabelaAtribuida.getSelectedRows();
        if (rows.length == 0) {
            msg = new Toast("Você tem que fazer uma seleção dos alunos a apagar para a aula", 3000);
            msg.showToast();
        } else {
            try {

                for (int row : rows) {
                    main.control.getAlunoAulaDAO().delete(list_atual_atribuidos.get(row).getId());
                    aula = main.control.getAulaDAO().get(aula.getId());
                }
                updateList(aula);
                updateJTable();
                updateJTableAtribuida();
            } catch (BussinessException ex) {
                main.control.messageErroBussiness(ex);
            }

        }
    }//GEN-LAST:event_naoAtribuirActionPerformed

