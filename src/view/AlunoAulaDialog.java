    public void setAula(Aula aula) {
        this.aula = aula;
        jLabel2.setText(("AULA: " + (aula.getNumero()<10?"0"+aula.getNumero():aula.getNumero()+"")+
                " - PROFESSOR: "+aula.getProfessor().getFuncionario().getPessoa().getNome()+
                " - TIPO: "+aula.getTipo().getNome()).toUpperCase());
        updateList(aula);
        updateJTable();
        updateJTableAtribuida();
    }

    public void addRowTableModel(DefaultTableModel model, Aluno obj) {
        model.addRow(new Object[]{
            obj.getPessoa().getNome()+" "+obj.getPessoa().getSobrenome()
        });
    }

    public void addRowTableModelAtribuida(DefaultTableModel model, AlunoAula obj) {
        model.addRow(new Object[]{
            obj.getAluno().getPessoa().getNome()+" "+obj.getAluno().getPessoa().getSobrenome()
        });
    }

    public void updateList(Aula obj) {
        list_atual = new ArrayList<Aluno>();
        list_atual_atribuidos = new ArrayList<AlunoAula>();
        for (AlunoAula salest : obj.getAlunoAulas()) {
            list_atual_atribuidos.add(salest);
        }

        try {
            List<Aluno> objs = main.control.getAlunoDAO().findAll();
            for (Aluno atr : objs) {
                boolean flag = false;
                for (AlunoAula aden : list_atual_atribuidos) {
                    if (aden.getAluno().getId() == atr.getId()) {
                        flag = true;
                        break;
                    }
                }
                if (!flag) {
                    list_atual.add(atr);
                }
            }
        } catch (BussinessException ex) {
            main.control.messageErroBussiness(ex);
        }

    }

    public void updateJTable() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);
        for (Aluno obj : list_atual) {
            addRowTableModel(model, obj);
        }
    }

    public void updateJTableBusca(List<Aluno> list) {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);
        for (Aluno obj : list) {
            addRowTableModel(model, obj);
        }
    }

    public void updateJTableAtribuida() {
        DefaultTableModel model = (DefaultTableModel) tabelaAtribuida.getModel();
        model.setNumRows(0);
        for (AlunoAula obj : aula.getAlunoAulas()) {
            addRowTableModelAtribuida(model, obj);
        }
    }

    public void updateJTableBuscaAtribuida(List<AlunoAula> list) {
        DefaultTableModel model = (DefaultTableModel) tabelaAtribuida.getModel();
        model.setNumRows(0);
        for (AlunoAula obj : list) {
            addRowTableModelAtribuida(model, obj);
        }
    }

